SELECT
  (SELECT COUNT(*) FROM airports WHERE country_code='IN') AS indian_airports,
  (SELECT COUNT(*) FROM airports WHERE country_code!='IN') AS intl_hubs,
  (SELECT COUNT(*) FROM airlines) AS airlines,
  (SELECT COUNT(*) FROM routes) AS total_routes,
  (SELECT COUNT(DISTINCT origin_code) FROM routes
   WHERE origin_code IN (SELECT iata_code FROM airports WHERE country_code='IN')) AS airports_with_routes;

