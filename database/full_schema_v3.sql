INSERT INTO routes (origin_code,destination_code,distance_km,avg_duration_min,airlines,min_price_inr,avg_price_inr,max_price_inr,flights_per_day)
SELECT
  destination_code, origin_code, distance_km, avg_duration_min,
  airlines, min_price_inr, avg_price_inr, max_price_inr, flights_per_day
FROM routes
ON CONFLICT (origin_code, destination_code) DO NOTHING;

